package jp.co.jjs.java_seminar.self_study.q5_4;

public class Sphere {
    double r;
    double pointx;
    double pointy;
    double pointz;

    public Sphere() {
        r = 1;
        pointx = 0;
        pointy = 0;
        pointz = 0;
    }

    public Sphere(double rr) {
        r = rr;;
        pointx = 0;
        pointy = 0;
        pointz = 0;
    }

    public Sphere(double rr, double xx, double yy, double zz) {
        rr = r;
        pointx = xx;
        pointy = yy;
        pointz = zz;
    }


}
